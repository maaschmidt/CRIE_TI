    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        try {
            boolean validacao = validarCampos(txtNome.getText(), txtUser.getText(), txtSenha.getText(), txtSenhaConf.getText());
            if (validacao) {

                preencherObjeto();//tem nome, login e senha
                if (objUsuario != null) {
                    controller = new UserController();
                    boolean retorno = false;
                    if (objUsuario.getId() == 0) {
                        retorno = controller.incluir(objUsuario);
                    } else {
                        retorno = controller.alterar(objUsuario);
                    }
                    if (retorno) {
                        Globais.exibirMensagem("Usuário Cadastrado");
                    } else {
                        Globais.exibirMensagem("Erro ao cadastrar usuário");
                    }
                }
            }
        } catch (Exception ex) {
            Globais.exibirMensagem("Erro ao salvar!");
        }
        controller.preencher(tblUsuarios);
    }//GEN-LAST:event_btnInserirActionPerformed


    private boolean validarCampos(String nome, String login, String senha, String senhaConf) {
        if (nome.equals("")) {
            Globais.exibirMensagem("Nome inválido");
            return false;
        } else if (login.equals("")) {
            Globais.exibirMensagem("User inválido");
            return false;
        } else if (senha.equals("")) {
            Globais.exibirMensagem("Senha não foi preenchida");
            return false;
        } else if (senhaConf.equals("")) {
            Globais.exibirMensagem("Confirmação de senha não foi preenchida");
            return false;
        }
        if (!senha.equals(senhaConf)) {
            Globais.exibirMensagem("Senhas inseridas são diferentes");
            return false;
        }
        return true;
    }

    private Usuario preencherObjeto() {
        try {
            objUsuario = new Usuario();
            objUsuario.setNome(txtNome.getText());
            objUsuario.setLogin(txtUser.getText());
            objUsuario.setPass(txtSenha.getText());
            return objUsuario;
        } catch (NumberFormatException ex) {
            System.out.println("Erro numérico " + ex.getMessage());
            return null;
        } catch (Exception ex) {
            System.out.println("Erro: " + ex.getMessage());
            return null;
        }
    }


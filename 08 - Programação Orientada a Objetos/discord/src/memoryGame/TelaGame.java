    private boolean confere(Icon icone, JButton btn){
        if (btnJogada1 == null){
            btnJogada1 = btn;
        }
        if (jogada1 == jogadaConfere){
            jogada1 = icone;
        } else if (jogada1 == icone){
            acerto++;
            jogada1 = jogadaConfere;
            btnJogada1 = null;
            lblAcertos.setText(String.valueOf(acerto));
        } else {
            erro++;
            jogada1 = jogadaConfere;
            btnJogada1 = null;
            lblErros.setText(String.valueOf(erro));
            return true;
        }
        return false;
    }
    
